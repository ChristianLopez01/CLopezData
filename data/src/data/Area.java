package data;

public class Area {
	 public static void main(String[] args) {
		 System.out.println("Data Structure");
	 }
	 public static double AreaC(int r){
		 return 3.14*r*r;
	 }
	 public static double HalfAreaC(int r){
		 return (3.14*r*r)/2;
	 }
	 public static double PerimeterC(int r){
		 return 2*3.14*r;
	 }
}
